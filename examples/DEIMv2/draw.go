// ── 描画プリミティブ ──

func drawRectThick(img *image.RGBA, x1, y1, x2, y2 int, c color.RGBA, thick int) {
	b := img.Bounds()
	for t := 0; t < thick; t++ {
		for x := x1; x <= x2; x++ {
			if y1+t >= b.Min.Y && y1+t < b.Max.Y {
				img.SetRGBA(clampI(x, b.Min.X, b.Max.X-1), y1+t, c)
			}
			if y2-t >= b.Min.Y && y2-t < b.Max.Y {
				img.SetRGBA(clampI(x, b.Min.X, b.Max.X-1), y2-t, c)
			}
		}
		for y := y1; y <= y2; y++ {
			if x1+t >= b.Min.X && x1+t < b.Max.X {
				img.SetRGBA(x1+t, clampI(y, b.Min.Y, b.Max.Y-1), c)
			}
			if x2-t >= b.Min.X && x2-t < b.Max.X {
				img.SetRGBA(x2-t, clampI(y, b.Min.Y, b.Max.Y-1), c)
			}
		}
	}
}

func fillRect(img *image.RGBA, x1, y1, x2, y2 int, c color.RGBA) {
	b := img.Bounds()
	for y := max(y1, b.Min.Y); y <= min(y2, b.Max.Y-1); y++ {
		for x := max(x1, b.Min.X); x <= min(x2, b.Max.X-1); x++ {
			img.SetRGBA(x, y, c)
		}
	}
}

// ── テキスト描画 (5x7 ビットマップフォント) ──

func drawText(img *image.RGBA, x, y int, text string, c color.RGBA) {
	for _, ch := range text {
		idx := int(ch) - 32
		if idx < 0 || idx >= len(font5x7) {
			idx = 0
		}
		glyph := font5x7[idx]
		for row := 0; row < 7; row++ {
			for col := 0; col < 5; col++ {
				if glyph[row]&(1<<(4-col)) != 0 {
					img.SetRGBA(x+col, y+row, c)
				}
			}
		}
		x += 6
	}
}

